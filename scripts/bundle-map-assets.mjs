#!/usr/bin/env node
// Bundles Three.js + GLTFLoader and all map assets into a single TS file.
// Run: node scripts/bundle-map-assets.mjs
import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from "fs";
import { resolve, join, extname, basename } from "path";
import { execSync } from "child_process";
import { writeFile, unlink } from "fs/promises";

const ROOT = resolve(import.meta.dirname, "..");
const ASSETS_DIR = join(ROOT, "apps/mobile/assets/map");
const GEN_DIR = join(ROOT, "apps/mobile/src/generated");
const OUT_FILE = join(GEN_DIR, "mapBundle.ts");

if (!existsSync(GEN_DIR)) mkdirSync(GEN_DIR, { recursive: true });

// ── 1. Bundle Three.js + GLTFLoader ─────────────────────────────────────────

// Entry must live inside the project so esbuild finds node_modules
const ENTRY = join(ROOT, "_map_bundle_entry.mjs");
await writeFile(
  ENTRY,
  `import * as THREE from "three";
import { GLTFLoader } from "three/examples/jsm/loaders/GLTFLoader.js";
globalThis.THREE = THREE;
globalThis.THREE.GLTFLoader = GLTFLoader;
`
);

console.log("Bundling Three.js + GLTFLoader…");
const threeBundle = execSync(
  `"${join(ROOT, "node_modules/.bin/esbuild")}" "${ENTRY}" --bundle --format=iife --minify --platform=browser`,
  { cwd: ROOT }
).toString();
await unlink(ENTRY);
console.log(`  three bundle: ${(threeBundle.length / 1024).toFixed(0)} KB`);

// ── 2. Read all map assets ───────────────────────────────────────────────────

// Cross-floor textures: file is in a different floor directory than the key's floor prefix.
// These need a separate key so the canonical texture and the cross-floor version coexist.
const CROSS_FLOOR = {
  "floor2/1_38.webp": "xf_2_1.38",
  "floor4/3_56.webp": "xf_4_3.56",
};

// Textures canonically keyed with a non-matching floor prefix (their key IS in TEXTURE_MODULES).
const CANONICAL_OVERRIDE = new Set(["3_59.webp", "3_61.webp", "3_62.webp"]);

const SPECIAL = new Set(["bathrooms", "lifts", "stairs"]);
const FLOORS = ["1", "2", "3", "4"];
const FLOOR_DIRS = { "1": "floor1", "2": "floor2", "3": "floor3", "4": "floor4" };

const assets = {};
const roomKeys = [];

function b64(path) {
  return readFileSync(path).toString("base64");
}

for (const floor of FLOORS) {
  const dir = join(ASSETS_DIR, FLOOR_DIRS[floor]);
  if (!existsSync(dir)) continue;

  for (const file of readdirSync(dir)) {
    const ext = extname(file);
    const name = basename(file, ext);
    const fullPath = join(dir, file);
    const relKey = `${FLOOR_DIRS[floor]}/${file}`;

    if (ext === ".glb") {
      assets[`glb_${floor}`] = b64(fullPath);
      console.log(`  glb_${floor}: ${(readFileSync(fullPath).length / 1024).toFixed(0)} KB`);
      continue;
    }

    if (ext !== ".webp") continue;

    if (name === "none") {
      assets[`none_${floor}`] = b64(fullPath);
      continue;
    }

    if (SPECIAL.has(name)) {
      assets[`sp_${floor}_${name}`] = b64(fullPath);
      continue;
    }

    // Room texture — derive key: "1_4" → "1.4", "3_27a" → "3.27a"
    if (CROSS_FLOOR[relKey]) {
      assets[CROSS_FLOOR[relKey]] = b64(fullPath);
      continue;
    }

    const roomKey = name.replace("_", ".");
    assets[roomKey] = b64(fullPath);
    if (!CANONICAL_OVERRIDE.has(file)) {
      // Only push if the room belongs to this floor's picker
      const roomFloor = roomKey.split(".")[0];
      if (roomFloor === floor) roomKeys.push(roomKey);
    } else {
      roomKeys.push(roomKey);
    }
  }
}

console.log(`  ${Object.keys(assets).length} assets collected`);

// ── 3. Write generated TypeScript ────────────────────────────────────────────

const lines = [
  "// Auto-generated by scripts/bundle-map-assets.mjs — do not edit",
  "",
  `export const THREE_BUNDLE = ${JSON.stringify(threeBundle)};`,
  "",
  "export const MAP_ASSETS: Record<string, string> = {",
  ...Object.entries(assets).map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`),
  "};",
  "",
  `export const ROOM_KEYS: string[] = ${JSON.stringify(roomKeys)};`,
];

writeFileSync(OUT_FILE, lines.join("\n"));
const outSize = readFileSync(OUT_FILE).length;
console.log(`Written: ${OUT_FILE} (${(outSize / 1024 / 1024).toFixed(1)} MB)`);
