#!/usr/bin/env node
// Pre-bundles all map assets (GLB + WebP textures) as base64 string literals.
// Mirrors the approach proven on the `test/` branch: avoids `expo-asset` /
// `expo-file-system` at runtime, which has been unreliable in release builds.
// Run: pnpm bundle:map
import { readFileSync, writeFileSync, readdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const MAP_DIR = join(ROOT, "assets", "map");
const OUT_PATH = join(ROOT, "src", "lib", "mapAssets.ts");

const FLOORS = ["1", "2", "3", "4"];

// Rooms that appear on a non-canonical floor (also exist on their primary floor).
// Keep in sync with d17map/index.tsx CROSS_FLOOR_TEXTURES.
const CROSS_FLOOR = {
  "2": new Set(["1.38"]),
  "4": new Set(["3.56"]),
};

const SPECIAL_NAMES = new Set(["bathrooms", "lifts", "stairs"]);

function toBase64(path) {
  return readFileSync(path).toString("base64");
}

function fileKeyFromName(name) {
  // "1_4.webp" -> "1.4", "3_27a.webp" -> "3.27a"
  return name.replace(/\.webp$/, "").replace("_", ".");
}

const glb = {};
const textures = {};
const special = { "1": {}, "2": {}, "3": {}, "4": {} };
const crossFloor = { "1": {}, "2": {}, "3": {}, "4": {} };

for (const floor of FLOORS) {
  const floorDir = join(MAP_DIR, `floor${floor}`);
  const entries = readdirSync(floorDir);

  for (const entry of entries) {
    const full = join(floorDir, entry);

    if (entry === "model.glb") {
      glb[floor] = toBase64(full);
      continue;
    }
    if (entry === "none.webp") {
      textures[`none${floor}`] = toBase64(full);
      continue;
    }
    if (entry.endsWith(".webp")) {
      const base = entry.replace(/\.webp$/, "");
      if (SPECIAL_NAMES.has(base)) {
        special[floor][base] = toBase64(full);
        continue;
      }
      const key = fileKeyFromName(entry);
      if (CROSS_FLOOR[floor]?.has(key)) {
        crossFloor[floor][key] = toBase64(full);
      } else {
        textures[key] = toBase64(full);
      }
    }
  }
}

function objLiteral(obj, indent = "  ") {
  const keys = Object.keys(obj).sort();
  if (keys.length === 0) return "{}";
  const entries = keys.map(
    (k) => `${indent}${JSON.stringify(k)}: ${JSON.stringify(obj[k])},`,
  );
  return `{\n${entries.join("\n")}\n${indent.slice(2)}}`;
}

function nestedLiteral(obj, indent = "  ") {
  const keys = Object.keys(obj).sort();
  if (keys.length === 0) return "{}";
  const entries = keys.map(
    (k) => `${indent}${JSON.stringify(k)}: ${objLiteral(obj[k], indent + "  ")},`,
  );
  return `{\n${entries.join("\n")}\n${indent.slice(2)}}`;
}

const ts = [
  "// Auto-generated by scripts/bundle-map-assets.mjs — do not edit manually.",
  "// Rebuild: pnpm bundle:map",
  "",
  `export const MAP_GLB: Record<string, string> = ${objLiteral(glb)};`,
  "",
  `export const MAP_TEXTURES: Record<string, string> = ${objLiteral(textures)};`,
  "",
  `export const MAP_SPECIAL_TEXTURES: Record<string, Record<string, string>> = ${nestedLiteral(special)};`,
  "",
  `export const MAP_CROSS_FLOOR_TEXTURES: Record<string, Record<string, string>> = ${nestedLiteral(crossFloor)};`,
  "",
].join("\n");

writeFileSync(OUT_PATH, ts);

const mb = (ts.length / (1024 * 1024)).toFixed(2);
console.log(
  `mapAssets.ts updated (${mb} MB): ${Object.keys(glb).length} GLB, ${Object.keys(textures).length} textures, ` +
    `${Object.values(special).reduce((n, m) => n + Object.keys(m).length, 0)} specials, ` +
    `${Object.values(crossFloor).reduce((n, m) => n + Object.keys(m).length, 0)} cross-floor.`,
);
